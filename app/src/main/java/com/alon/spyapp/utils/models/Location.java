package com.alon.spyapp.utils.models;

public class Location {

    private Double lat, lng;

    // Constructor.
    public Location() {
    }

    // Getters and setters.
    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLng() {
        return lng;
    }

    public void setLng(Double lng) {
        this.lng = lng;
    }
}
